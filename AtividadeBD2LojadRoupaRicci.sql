
CREATE DATABASE BD_LOJA_RICCI;
USE BD_LOJA_RICCI;

CREATE TABLE TBCLIENTE (
  IDCLIENTE INT AUTO_INCREMENT PRIMARY KEY,
  NOMECLI VARCHAR(30),
  SEXOCLI CHAR(1),
  NASCLI DATE,
  ESTADOCLI VARCHAR(15),
  CPFCLI VARCHAR(15),
  RGCLI VARCHAR(30),
  RUA VARCHAR(30),
  NUMERO VARCHAR(5),
  COMPLEMENTO VARCHAR(10),
  CEP CHAR(8),
  CIDADE VARCHAR(20),
  ESTADO VARCHAR(20),
  TELFIXO VARCHAR(13),
  TELCEL VARCHAR(13),
  EMAIL_CLI VARCHAR(30),
  STATUSCLI VARCHAR(7),
  DATA_CADASTRO DATETIME
) ENGINE=INNODB;


CREATE TABLE TBFORNECEDOR (
  IDFORN INT AUTO_INCREMENT PRIMARY 
  NOME_FORN VARCHAR(30),
  CNPJ_FORN VARCHAR(30),
  RUA_FORN VARCHAR(30),
  NUMERO_FORN VARCHAR(10),
  COMPLEMENTOFORN VARCHAR(20),
  BAIRROFORN VARCHAR(20),
  CEPFORN VARCHAR(10),
  CIDADEFORN VARCHAR(20),
  ESTADOFORN VARCHAR(20),
  TELEFONEFIXO_FORN VARCHAR(13),
  TELCELL VARCHAR(13),
  EMAILFORN VARCHAR(30),
  NOMECONTATOFORN VARCHAR(30),
  OBSFORN VARCHAR(50),
  STATUSFORN VARCHAR(7),
  DATA_CADASTRO DATETIME
) ENGINE=INNODB;


CREATE TABLE TB_FUNCIONARIO (
  IDFUNC INT AUTO_INCREMENT PRIMARY KEY,
  NOMEFUNC VARCHAR(30),
  SEXOFUNC CHAR(1),
  DATANASFUNC DATE,
  RGFUNC VARCHAR(20),
  CPFFUNC VARCHAR(20)
) ENGINE=INNODB;


CREATE TABLE TBPRODUTO (
  IDPRODUTO INT AUTO_INCREMENT PRIMARY KEY,
  COD_FORNECEDOR INT,
  NOMEPROD VARCHAR(30),
  TAMANHOPROD VARCHAR(10),
  PRECOCOMPROD DECIMAL(7,2),
  PRECOVEPROD DECIMAL(7,2),
  QUANTIDADEPROD INT,
  OBSPROD VARCHAR(50),
  STATUSPROD VARCHAR(7),
  DATAULTIMOFORN DATE,
  MARCAPROD VARCHAR(20),
  FOREIGN KEY (COD_FORNECEDOR) REFERENCES TBFORNECEDOR(IDFORN)
) ENGINE=INNODB;


CREATE TABLE TBVENDA (
  IDVENDA INT AUTO_INCREMENT PRIMARY KEY,
  COD_FUNCIONARIO INT,
  COD_CLIENTE INT,
  DESCONTO_VEND VARCHAR(4),
  DATAVEND DATETIME,
  FOREIGN KEY (COD_FUNCIONARIO) REFERENCES TB_FUNCIONARIO(IDFUNC),
  FOREIGN KEY (COD_CLIENTE) REFERENCES TBCLIENTE(IDCLIENTE)
) ENGINE=INNODB;


CREATE TABLE TBITENSVENDA (
  IDITEMVENDA INT AUTO_INCREMENT PRIMARY KEY,
  IDVENDA INT,
  IDPRODUTO INT,
  QUANTIDADE INT,
  PRECOUNIT DECIMAL(7,2),
  FOREIGN KEY (IDVENDA) REFERENCES TBVENDA(IDVENDA),
  FOREIGN KEY (IDPRODUTO) REFERENCES TBPRODUTO(IDPRODUTO)
) ENGINE=INNODB;


